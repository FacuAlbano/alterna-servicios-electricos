function forceInitializeTranslator() {
    console.log('üîß FORCE initializing translator...');
    
    // Verificar elementos m√∫ltiples veces
    let attempts = 0;
    const maxAttempts = 20; // 2 segundos m√°ximo
    
    const checkAndInit = () => {
        attempts++;
        console.log(`üîç Attempt ${attempts}/${maxAttempts} - Looking for translator elements...`);
        
        const langToggle = document.querySelector('.language-toggle');
        const translateOptions = document.querySelector('.translate-options');
        const translateButtons = document.querySelectorAll('.translate-option');
        
        if (langToggle && translateOptions && translateButtons.length > 0) {
            console.log('‚úÖ All elements found! Initializing...');
            initializeTranslator();
            return true;
        } else {
            console.log('‚ùå Elements missing:', {
                langToggle: !!langToggle,
                translateOptions: !!translateOptions,
                translateButtons: translateButtons.length
            });
            
            if (attempts < maxAttempts) {
                setTimeout(checkAndInit, 100);
                return false;
            } else {
                console.error('üí• Failed to initialize translator after max attempts');
                // Crear elementos manualmente como backup
                createTranslatorBackup();
                return false;
            }
        }
    };
    
    checkAndInit();
}

